//Símbolo de = atribui o valor 7 a constante "a"
const a = 7
let b = 3

//Atribuição aditiva, somando o valor de "a" a "b"
b += a
console.log(b)

//Atribuição subtrativa...
b -= 4
console.log(b)

//Atribuição multiplicativa
b *= 2
console.log(b)

// Atribuição divisiva
b /= 2
console.log(b)

//Atribuição "modulo" pega o resto da divisão
b %= 2
console.log(b)
